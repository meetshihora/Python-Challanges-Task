''' Task 1: Write a function called calculate_area that takes base and height as an input and returns and area of a triangle. Equation of an area of a triangle is,
        area = (1/2)*base*height '''

# Solution
def calculate_area(base,height):
    area = (1/2)*base*height
    return area 

print(calculate_area(3,4))

# Output
# 6.0