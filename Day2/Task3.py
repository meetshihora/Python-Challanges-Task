# Task 3: You want to replace tiles in your bathroom which is exactly square and 5.5 feet is its length. If tiles cost 500 rs per square feet, how much will be the total cost to replace all tiles. Calculate and print the cost using python (Hint: Use power operator ** to find area of a square)


# Solution

length = 5.5
cost = 500
area = length**2
totalCost = area*cost
print(totalCost)

# Output: 15125.0