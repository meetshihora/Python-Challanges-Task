# Task 2: You bought 9 packets of potato chips from a store. Each packet costs 1.49 dollar and you gave shopkeeper 20 dollar. Find out using python, how many dollars is the shopkeeper going to give you back?

# Solution:
numOfPackets = 9
costOfPackets = 1.49
totalCost = numOfPackets * costOfPackets
paid = 20
cashBack = paid - totalCost
print("Cash Back:", cashBack)

# Output: Cash Back: 6.59